function [S,R,err] = ZD2SR(Z,D,C,U)

    k = 8; % number of true roots
    z = nan(size(C,2),size(Z,2)); 
    for i = 1:size(Z,2); z(:,i) = U(Z(:,i)); end
    z = z./vecnorm(z);
    e = vecnorm(C*z);
    [e,I] = sort(e);
    Z = Z(:,I(1:k)); % filter out false roots
    err = e(1:k);
    c = ~any(imag(Z),1) & all(Z>0,1); % real and positive roots only
    L = Z(:,c);
    err = err(c);

    nr = size(L,2);
    S = nan(2,size(D,2),nr);
    R = nan(2,size(D,1),nr);

    r = 0; % number of real solutions
    for i = 1:nr
        [Sr,Rr,flag] = DL2SR(D,L(:,i));
        if ~flag
            r = r + 1;
            S(:,:,r) = Sr;
            R(:,:,r) = Rr;
            err(r) = err(i);
        end
    end
    S = S(:,:,1:r);
    R = R(:,:,1:r);

end



function [S,R,flag] = DL2SR(D,L)

    flag = 0;
    n = size(D,1);
    m = size(D,2);
    S = zeros(2,m);
    R = zeros(2,n);
    x2 = sqrt(L(1));
    S(:,2) = [x2; 0];
    x3 = (L(1) + L(2) - L(3))/(2*x2);
    x3sq = x3^2;
    if L(2) <= x3sq; flag = 1; return; end
    y3 = sqrt(L(2) - x3sq);
    S(:,3) = [x3; y3];
    a1 = L(1) + (D(:,1) - D(:,2))';
    a2 = L(2) + (D(:,1) - D(:,3))';
    R = [a1; (x2*a2 - x3*a1)./y3]./(2*x2);

end