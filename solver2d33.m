% 2D 3s/3r ToA self-localization
%
% input:
%  D     - 3x3 matrix of squared distances between senders and receivers
%  itRef - iterative refinement flag
%
% output:
%  S   - 2x3xk array of 2x3 (strictly upper triangular) matrices with sender positions
%  R   - 2x3xk array of 2x3 matrices with receiver positions
%  err - k-vector of errors
function [S,R,err] = solver2d33(D,itRef)

    [C,U] = coefs2d33(D);
    Z = red_18x28_toa2d_33(C);
    [S,R,err] = ZD2SR(Z,D,C,U);
    
    if itRef; [S,R,err,~] = iterref(S,R,D); end

end



function [C,U] = coefs2d33(D)

    C = zeros(3,10);

    C(1:3) = -1;
    C([19:21,10:12,4:6]) = -D;
    C(7:9) = D(4:6)+D(7:9);
    C(13:15) = D(1:3)+D(7:9);
    C(16:18) = D(1:3)+D(4:6);
    C(28:30) = -D(1:3).^2-D(4:6).*D(7:9)+D(1:3).*D(7:9)+D(4:6).*D(1:3);
    C(25:27) = -D(4:6).^2+D(4:6).*D(7:9)-D(1:3).*D(7:9)+D(4:6).*D(1:3);
    C(22:24) = -D(7:9).^2+D(4:6).*D(7:9)+D(1:3).*D(7:9)-D(4:6).*D(1:3);

    C = C./vecnorm(C,2,2);

    U = @(s)[s(1)*s(2)*s(3);s(1)^2;s(2)*s(1);s(2)^2;s(3)*s;s];

end



