% generate TOA
function toa = genTOA(toaType)

    if toaType == 1
        S = [zeros(2,1) triu(rand(2,3-1))];
        R = rand(2,3);
        solver = 'solver2d33';
    elseif toaType == 2
        S = [zeros(3,1) triu(rand(3,4-1))];
        R = rand(3,6);
        solver = 'solver3d46';
    elseif toaType == 101
        S = [0                 0.369620890922983 0.696745099879249;
             0                 0                 0.971196157648198];
        R = [0.482859327946673 0.111087493175355 0.382699445739376;
             0.578728907962057 0.449615815301317 0.511157976054347];
        solver = 'solver2d33';
    elseif toaType == 102
        S = [0                 0.236692029330000 0.171913677768761 0.972694449266300;
             0                 0                 0.510098473800336 0.835366678718201;
             0                 0                 0                 0.415615042619187];
        R = [0.548942037634695 0.485146181903973 0.093947280933017 0.664637262792736 0.818684500339317 0.838398621970246;
             0.049617719728601 0.781052228573005 0.692409828782822 0.043955966351070 0.701680566705667 0.710251338336607;
             0.362684653562586 0.756103847635523 0.727404084916511 0.369040371464452 0.732403407226110 0.748037200679719];
        solver = 'solver3d46';
    else
        error('Invalid toaType');
    end

    toa.D = SR2D(S,R);
    toa.Sgt = S;
    toa.Rgt = R;
    toa.solver = solver;

end



function D = SR2D(S,R)

    m = size(S,2);
    n = size(R,2);
    D = zeros(n,m); % squared distances
    for i = 1:m
        for k = 1:n
            D(k,i) = (S(:,i) - R(:,k))'*(S(:,i) - R(:,k));
        end
    end

end